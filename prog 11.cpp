#include <iostream>
using namespace std;

int main() {
    
    // Declare and initialize two integer variables
    int num1 = 10;
    int num2 = 20;

    // Calculate average
    double average = (num1 + num2) / 2.0;  // divide by 2.0 to get decimal result

    // Output the result
    cout << "The average of " << num1 << " and " << num2 << " is: " << average << endl;

    return 0;
}
