#include <iostream>
using namespace std;

int main() {
    int n;

    // Input value of n
    cout << "Enter a value for n: ";
    cin >> n;

    // Check condition
    if (n > 100) {
        cout << "The value of n is greater than 100." << endl;
    }

    // If n <= 100, nothing is printed

    return 0;
}
